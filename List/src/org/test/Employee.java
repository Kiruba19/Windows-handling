package org.test;

public class Employee {
	
		private int Id;
		private String Name;
		private Float sal;
		
		public int getId() {
			return Id;
		}
		public void setId(int id) {
			Id = id;
		}
		
		
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public Float getSal() {
		return sal;
	}
	public void setSal(Float sal) {
		this.sal = sal;
	}
	

}
